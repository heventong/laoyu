        function mtaphold(tag, backcall) {
            var timeOutEvent = 0;
            $(tag).bind('touchstart', function(e) {
                var obj = this;
                timeOutEvent = setTimeout(function() {
                    timeOutEvent = 0;
                    if (backcall) backcall(e, obj);
                }, 500);
            }).bind('touchmove', function(e) {
                clearTimeout(timeOutEvent);
                timeOutEvent = 0;
            }).bind('touchend', function(e) {
                clearTimeout(timeOutEvent);
                timeOutEvent = 0;
            });
        }

